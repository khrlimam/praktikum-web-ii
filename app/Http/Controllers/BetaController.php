    /**
     * Remove the specified resource from storage.
     *
     * @param int $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $deleted = Beta::find($id)->delete();
        if ($deleted)
            return redirect()->route('beta.index')->with('status', 'Data dengan id ' . $id . ' telah dihapus!');
    }
